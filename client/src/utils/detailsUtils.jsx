/**
 * Manejo de carga de archivos/imágenes
 */
export const handleFileUpload = async (file, entityId, entityType, token) => {
  // Validar tipo de archivo
  if (!file.type.startsWith('image/')) {
    throw new Error('Por favor selecciona un archivo de imagen válido');
  }

  // Validar tamaño (máximo 5MB)
  if (file.size > 5 * 1024 * 1024) {
    throw new Error('El archivo es demasiado grande. Máximo 5MB');
  }

  const formData = new FormData();
  formData.append('image', file);

  const response = await fetch(`${import.meta.env.VITE_API_URL || 'http://localhost:4000/api'}/${entityType}/${entityId}/upload`, {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${token}`
    },
    body: formData
  });

  if (!response.ok) {
    throw new Error('Error al subir la imagen');
  }

  return response.json();
};

/**
 * Renderizar campo con icono
 */
export const renderFieldWithIcon = (label, value, icon, link = null) => {
  if (!value) return null;

  const content = (
    <div className="mt-1 p-3 bg-gray-50 border border-gray-200 rounded-md flex items-center">
      {icon}
      {link ? (
  <a href={link} className="text-brand-700 hover:text-brand-900 ml-2">
          {value}
        </a>
      ) : (
        <span className="ml-2">{value}</span>
      )}
    </div>
  );

  return (
    <div>
      <label className="block text-sm font-medium text-gray-700 mb-2">{label}</label>
      {content}
    </div>
  );
};

/**
 * Renderizar campo simple
 */
export const renderField = (label, value, className = "sm:col-span-1") => {
  if (!value) return null;

  return (
    <div className={className}>
      <label className="block text-sm font-medium text-gray-700 mb-2">{label}</label>
      <div className="mt-1 p-3 bg-gray-50 border border-gray-200 rounded-md">
        {value}
      </div>
    </div>
  );
};

/**
 * Renderizar estadística
 */
export const renderStat = (value, label, bgColor = "bg-brand-50", textColor = "text-brand-700") => {
  return (
    <div className={`text-center p-4 ${bgColor} rounded-lg`}>
      <div className={`text-2xl font-bold ${textColor}`}>
        {value || 0}
      </div>
      <div className={`text-sm ${textColor}`}>{label}</div>
    </div>
  );
};

